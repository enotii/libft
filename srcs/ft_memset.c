#include "header.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char		*buff;
	size_t		i;

	buff = b;
	i = 0;
	while (i < len)
	{
		buff[i] = c;
		i++;
	}
	return (buff);
}
